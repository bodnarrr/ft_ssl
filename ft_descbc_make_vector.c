#include "ft_ssl_des.h"

uint64_t		ft_descbc_make_vector(char *str)
{
	int			i;
	uint64_t	res;

	res = 0;
	i = -1;
	while (++i < 8)
	{
		res <<= 8;
		res |= (str[i] & 255);
	}
	return (res);
}
